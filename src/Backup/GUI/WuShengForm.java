    public void getAllOnline(boolean Offline) {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int rows = tableModel.getRowCount();
        String online = "離線";
        for (int i = 0; i < rows; i++) {
            tableModel.removeRow(0);
        }
        if (!Offline) {
            online = "上線";
            for (ChannelServer cserv : ChannelServer.getAllInstances()) {
                for (MapleCharacter mch : cserv.getPlayerStorage().getAllCharacters()) {
                    tableModel.addRow(new Object[]{mch.getName(), online});
                }
            }
            return;
        }
        ResultSet rs_2 = getAllOnline();
        try {
            while (rs_2.next()) {
                for (ChannelServer channel : ChannelServer.getAllInstances()) {
                    MapleCharacter player = channel.getPlayerStorage().getCharacterByName(rs_2.getString("name"));
                    if (player != null && rs_2.getString("name").equals(player.getName())) {
                        online = "上線";
                        break;
                    } else {
                        online = "離線";
                    }
                }
                tableModel.addRow(new Object[]{rs_2.getString("name"), online});
            }
        } catch (Exception ex) {

        }
    }

    public void Query_Qata(String name) {
        MapleItemInformationProvider ii = MapleItemInformationProvider.getInstance();
        DefaultTableModel tableModel1 = (DefaultTableModel) jTable2.getModel();
        int rows1 = tableModel1.getRowCount();
        ResultSet rs_2;
        if (jTabbedPane1.getSelectedIndex() == 0) {
            //rs_2 = EquipData(name);
            rs_2 = ItemData(name);
        } else {
            rs_2 = ItemData(name);
        }
        for (int i = 0; i < rows1; i++) {
            tableModel1.removeRow(0);
        }
        try {
            while (rs_2.next()) {
                int itemid = rs_2.getInt("itemid");
                String Name = ii.getName(rs_2.getInt("itemid"));

                if (itemid / 1000000 == jTabbedPane1.getSelectedIndex() + 1 && itemid / 1000000 != -1 && jTabbedPane1.getSelectedIndex() != 0) {
                    tableModel1.addRow(new Object[]{Name, rs_2.getString("itemid"), rs_2.getString("quantity")
                    });
                } else if (jTabbedPane1.getSelectedIndex() == 0) {
                    tableModel1.addRow(new Object[]{Name, rs_2.getString("itemid"), rs_2.getString("str") + "|" + rs_2.getString("dex") + "|" + rs_2.getString("int") + "|" + rs_2.getString("luk"), rs_2.getString("level"),
                        rs_2.getString("watk"), rs_2.getString("matk"), rs_2.getString("wdef"), rs_2.getString("mdef")
                    });
                }
            }
        } catch (Exception ex) {
            System.err.println(ex);
        }
    }

    public void LoadingData() {
        // TODO add your handling code here:
        if (jTabbedPane2.getSelectedIndex() == 4 && jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "請選擇玩家名稱", "跳出視窗內容標題", JOptionPane.INFORMATION_MESSAGE);
        } else if (jTabbedPane2.getSelectedIndex() == 4 && jTable1.getSelectedRow() != -1) {
            jLabel3.setText("查詢 <" + jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "> 資料");
            //置中
            DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
            rightRenderer.setHorizontalAlignment(0);
            for (int a = 0; a < jTable2.getColumnCount(); a++) {
                jTable2.getColumnModel().getColumn(a).setCellRenderer(rightRenderer);
            }

            //載入表單
            Query_Qata(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            jTable2.setAutoscrolls(true);
        } else {
            jLabel3.setText(run.getOnline());
            getAllOnline(jCheckBox2.isSelected());
        }
    }

