    private void jTextField9CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextField9CaretUpdate
        // TODO add your handling code here:
        MapleItemInformationProvider ii = MapleItemInformationProvider.getInstance();
        if (!(StringUtil.isNumeric(jTextField9.getText())) || "".equals(jTextField9.getText()) || "-".equals(jTextField9.getText())) {
            return;
        }
        int ItemID = Integer.parseInt(jTextField9.getText());
        //return;
        if (ItemID > 0) {
            if (ItemID < 99) {
                jLabelSever6.setText("贈送物品： 錯誤代碼");
                return;
            } else if (!ii.itemExists(ItemID)) {
                jLabelSever6.setText("贈送物品： 這個道具不存在");
                return;
            } else if (GameConstants.isPet(ItemID)) {
                jLabelSever6.setText("贈送物品： 寵物道具請通過商城購買");
                return;
            }
            jLabelSever6.setText("贈送物品： " + ItemID + "   [" + ii.getName(ItemID) + "]");
        }
    }//GEN-LAST:event_jTextField9CaretUpdate

