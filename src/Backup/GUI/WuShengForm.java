    private void jButtonSever2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSever2ActionPerformed
        // TODO add your handling code here:
        jButtonSever1ActionPerformed(evt);
        if (StringUtil.isNumeric(jTextField5.getText())) {
            minutesLeft = Integer.parseInt(jTextField5.getText()) * 60;
            if (ts == null && (t == null || !t.isAlive())) {
                t = new Thread(ShutdownServer.getInstance());
                ts = EventTimer.getInstance().register(new Runnable() {
                    @Override
                    public void run() {
                        if (minutesLeft == 0) {
                            ShutdownServer.getInstance();
                            t.start();
                            ts.cancel(false);
                            return;
                        }
                        if (minutesLeft % 60 == 0 || (minutesLeft / 60 < 1 && minutesLeft % 30 == 0) || (minutesLeft / 60 < 1 && minutesLeft % 15 == 0)) {
                            World.Broadcast.broadcastMessage(MaplePacketCreator.serverNotice(0, "伺服器即將在 " + (minutesLeft / 60 > 0 ? minutesLeft / 60 + " 分鐘" : minutesLeft + " 秒") + "後關閉. 請盡速關閉精靈商人 並下線."));
                            System.out.println("伺服器即將在 " + (minutesLeft / 60 > 0 ? minutesLeft / 60 + " 分鐘" : minutesLeft + " 秒") + "後關閉.");
                        }
                        Minute = minutesLeft / 60;
                        second = minutesLeft % 60;
                        reciprocal = Minute + "分 " + second + "秒";
                        jLabelSever2.setText("伺服器倒數關閉：    " + reciprocal);
                        minutesLeft--;
                    }
                }, 1000);
            }
        } else {
            Thread t = null;
            System.out.println("關閉中...");
            if (t == null || !t.isAlive()) {
                t = new Thread(server.ShutdownServer.getInstance());
                t.start();
            } else {
                System.out.println("已在執行中...");
            }
        }
    }//GEN-LAST:event_jButtonSever2ActionPerformed

